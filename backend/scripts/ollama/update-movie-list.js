#!/usr/bin/env node

/**
 * CineMatch Movie List Updater
 * 
 * This script uses an Ollama AI model to generate new movie entries and similarity links
 * for the CineMatch database. It reads existing movies and similarities, prompts the AI
 * for unique additions, validates the response, and merges the new data back into the 
 * JSON data files.
 */

const fs = require('fs');
const https = require('https');
const path = require('path');
const { execFileSync } = require('child_process');

/**
 * Handles reading and writing JSON files containing arrays.
 */
class JsonFileRepository {
  constructor(filePath, expectedName) {
    this.filePath = filePath;
    this.expectedName = expectedName;
  }

  /**
   * Reads a JSON file and ensures it contains an array.
   */
  readArray() {
    const raw = fs.readFileSync(this.filePath, 'utf8');
    const parsed = JSON.parse(raw);

    if (!Array.isArray(parsed)) {
      throw new Error(`${this.expectedName} file must contain a JSON array.`);
    }

    return parsed;
  }

  /**
   * Writes an array to a JSON file with pretty formatting.
   */
  writeArray(items) {
    fs.writeFileSync(this.filePath, `${JSON.stringify(items, null, 2)}\n`, 'utf8');
  }
}

/**
 * Validates and sanitizes movie objects generated by the AI.
 */
class MovieValidator {
  static isNonEmptyString(value) {
    return typeof value === 'string' && value.trim().length > 0;
  }

  /**
   * Cleans movie data and checks against schema constraints.
   * Returns sanitized movie object or null if invalid.
   */
  static sanitize(movie) {
    if (!movie || typeof movie !== 'object') return null;

    const title = typeof movie.title === 'string' ? movie.title.trim() : '';
    const director = typeof movie.director === 'string' ? movie.director.trim() : '';
    const desc = typeof movie.desc === 'string' ? movie.desc.trim() : '';
    const poster = typeof movie.poster === 'string' ? movie.poster.trim() : '';

    const year = Number.parseInt(movie.year, 10);
    const duration = Number.parseInt(movie.duration, 10);

    const genres = Array.isArray(movie.genres)
      ? movie.genres
          .filter((genre) => typeof genre === 'string' && genre.trim().length > 0)
          .map((genre) => genre.trim())
      : [];

    const currentYear = new Date().getFullYear();
    const isValid =
      this.isNonEmptyString(title) &&
      this.isNonEmptyString(director) &&
      this.isNonEmptyString(desc) &&
      Number.isInteger(year) &&
      year >= 1950 &&
      year <= currentYear &&
      Number.isInteger(duration) &&
      duration >= 75 &&
      duration <= 240 &&
      genres.length > 0 &&
      genres.length <= 4;

    if (!isValid) return null;

    return { title, director, year, duration, desc, genres, poster };
  }
}

/**
 * Validates and sanitizes similarity pair arrays [topicA, topicB].
 */
class SimilarityValidator {
  /**
   * Cleans similarity pair data.
   * Returns sanitized pair [titleA, titleB] or null if invalid.
   */
  static sanitize(pair) {
    if (!Array.isArray(pair) || pair.length !== 2) return null;

    const first = typeof pair[0] === 'string' ? pair[0].trim() : '';
    const second = typeof pair[1] === 'string' ? pair[1].trim() : '';

    if (!first || !second || first.toLowerCase() === second.toLowerCase()) {
      return null;
    }

    return [first, second];
  }
}

/**
 * Executes a prompts to the Ollama CLI.
 */
class OllamaClient {
  constructor(modelName) {
    this.modelName = modelName;
  }

  /**
   * Invokes 'ollama run <model> <prompt>' and returns the raw output.
   */
  generate(prompt) {
    try {
      return execFileSync('ollama', ['run', this.modelName, prompt], {
        encoding: 'utf8',
        stdio: ['ignore', 'pipe', 'pipe'],
        maxBuffer: 10 * 1024 * 1024
      });
    } catch (error) {
      console.error(`[ollama-client] Failed to generate for model ${this.modelName}.`);
      console.error(`[ollama-client] Error: ${error.stderr || error.message}`);
      console.error(`[ollama-client] Hint: Ensure you've run 'bash backend/scripts/ollama/setup_ollama_agent.sh' to create the model.`);
      throw error;
    }
  }
}

/**
 * Handles web-based search for movie data using a sub-script.
 */
class SearchProvider {
  constructor(pythonPath) {
    this.pythonPath = pythonPath;
    this.scriptPath = path.resolve(__dirname, 'search_movies.py');
  }

  /**
   * Performs a search using the python bridge.
   */
  search(query) {
    try {
      const output = execFileSync(this.pythonPath, [this.scriptPath, query], {
        encoding: 'utf8',
        stdio: ['ignore', 'pipe', 'pipe']
      });
      return JSON.parse(output.trim());
    } catch (error) {
      console.warn(`[search-provider] Warning: Search failed for "${query}".`);
      return [];
    }
  }
}

/**
 * Fetches a poster image URL for a movie using the Wikipedia REST API.
 * Requires no API key. Tries common article naming patterns for films.
 */
class PosterFetcher {
  fetch(title, year) {
    const candidates = [
      `${title}_(${year}_film)`,
      `${title}_(film)`,
      title,
    ];

    const tryNext = (index) => {
      if (index >= candidates.length) return Promise.resolve('');
      const slug = encodeURIComponent(candidates[index].replace(/ /g, '_'));
      const url = `https://en.wikipedia.org/api/rest_v1/page/summary/${slug}`;
      return new Promise((resolve) => {
        const req = https.get(url, { headers: { 'User-Agent': 'CineMatch/1.0' } }, (res) => {
          let data = '';
          res.on('data', (chunk) => { data += chunk; });
          res.on('end', () => {
            try {
              const parsed = JSON.parse(data);
              if (res.statusCode === 200 && parsed.thumbnail && parsed.thumbnail.source) {
                return resolve(parsed.thumbnail.source);
              }
            } catch { /* ignore */ }
            resolve(tryNext(index + 1));
          });
        });
        req.on('error', () => resolve(tryNext(index + 1)));
        req.setTimeout(8000, () => { req.destroy(); resolve(tryNext(index + 1)); });
      });
    };

    return tryNext(0);
  }
}

/**
 * Builds structured prompts for the AI, including existing data context to avoid duplicates.
 */
class PromptBuilder {
  /**
   * Creates a prompt for generating new movie entries.
   */
  buildMoviePrompt(existingTitles, count) {
    // Pass the full title list so the model cannot regenerate any already-catalogued movie.
    return [
      `Generate ${count} unique, famous, real movies NOT in the exclusion list below.`,
      'Return JSON only: an array of movie objects with fields: "title", "director", "year", "duration", "desc", "genres", "poster".',
      'Rules:',
      '- Only return the JSON array [ { "title": "Example", ... }, ... ].',
      '- No Markdown. No JavaScript. No explanation.',
      '- Genres: Drama, Comedy, Sci-Fi, Thriller, Action, Romance, Horror, Adventure.',
      '- Poster: empty string.',
      `- Exclusion list — do not include ANY of these titles: ${JSON.stringify(existingTitles)}`,
    ].join('\n');
  }

}

/**
 * Computes genre-based similarity pairs for new movies against the full catalog.
 * Uses Jaccard similarity of genre sets — deterministic, no LLM hallucination.
 * Each new movie gets its top `topK` most genre-similar catalog partners.
 */
class SimilarityComputer {
  static computePairs(newMovies, allMovies, topK = 2) {
    const pairs = [];

    for (const newMovie of newMovies) {
      const newGenres = new Set((newMovie.genres || []).map((g) => g.toLowerCase()));
      if (newGenres.size === 0) continue;

      const scored = allMovies
        .filter((m) => m.title.toLowerCase() !== newMovie.title.toLowerCase())
        .map((m) => {
          const mGenres = new Set((m.genres || []).map((g) => g.toLowerCase()));
          const intersection = [...newGenres].filter((g) => mGenres.has(g)).length;
          const union = new Set([...newGenres, ...mGenres]).size;
          return { title: m.title, score: union > 0 ? intersection / union : 0 };
        })
        .filter((m) => m.score > 0)
        .sort((a, b) => b.score - a.score)
        .slice(0, topK);

      for (const match of scored) {
        pairs.push([newMovie.title, match.title]);
      }
    }

    return pairs;
  }
}

/**
 * Handles logic for merging generated data with existing data.
 */
class CatalogMerger {
  /**
   * Returns a list of movies with duplicates (by title) removed.
   */
  dedupeMoviesByTitle(movies) {
    // Normalize titles for comparison: lowercase, strip all non-alphanumeric characters.
    // This catches near-duplicates like "Se7en" vs "Seven", "Schindler's List" vs "Schindlers List".
    const normalize = (t) => t.toLowerCase().replace(/[^a-z0-9]/g, '');
    const seen = new Set();
    return movies.filter((movie) => {
      const key = normalize(movie.title);
      if (seen.has(key)) return false;
      seen.add(key);
      return true;
    });
  }

  /**
   * Merges existing and newly generated similarity pairs.
   * Ensures titles exist, removes duplicates (regardless of order), and self-references.
   */
  mergeSimilarities(existingPairs, generatedPairs, knownTitles) {
    const knownTitleKeys = new Set(knownTitles.map((title) => title.toLowerCase()));
    const pairKeySet = new Set();
    const merged = [];

    const pushPair = (pair) => {
      const sanitized = SimilarityValidator.sanitize(pair);
      if (!sanitized) return;

      const [a, b] = sanitized;
      const keyA = a.toLowerCase();
      const keyB = b.toLowerCase();

      if (!knownTitleKeys.has(keyA) || !knownTitleKeys.has(keyB)) return;

      const [left, right] = keyA < keyB ? [a, b] : [b, a];
      const pairKey = `${left.toLowerCase()}::${right.toLowerCase()}`;

      if (pairKeySet.has(pairKey)) return;

      pairKeySet.add(pairKey);
      merged.push([left, right]);
    };

    existingPairs.forEach(pushPair);
    generatedPairs.forEach(pushPair);

    return merged;
  }
}

/**
 * Coordinator for movie catalog updates. Orchestrates reading repositories, 
 * prompting the AI, merging results, and writing back.
 */
class MovieCatalogUpdater {
  constructor({
    moviesRepository,
    similaritiesRepository,
    ollamaClient,
    promptBuilder,
    catalogMerger,
    searchProvider,
    posterFetcher
  }) {
    this.moviesRepository = moviesRepository;
    this.similaritiesRepository = similaritiesRepository;
    this.ollamaClient = ollamaClient;
    this.promptBuilder = promptBuilder;
    this.catalogMerger = catalogMerger;
    this.searchProvider = searchProvider;
    this.posterFetcher = posterFetcher;
  }

  /**
   * Robust JSON extractor using brace-depth tracking.
   * Instead of trying to fix one big broken array string, it extracts each
   * {...} object individually, repairs each one, and assembles the array.
   * This handles the llama3.2:1b model's tendency to emit truncated arrays,
   * literal \" escapes, }} double-braces, and objects without commas between them.
   */
  extractJsonArray(rawResponse) {
    if (!rawResponse) throw new Error('Ollama result was empty.');

    // Step 1: Global pre-processing on the raw string
    let raw = rawResponse;

    // Unescape literal \" sequences the 1B model emits (actual backslash + quote chars)
    raw = raw.replace(/\\"/g, '"');

    // Remove control characters (newlines inside strings break JSON.parse)
    raw = raw.replace(/[\x00-\x09\x0B\x0C\x0E-\x1F\x7F]/g, ' ');
    // Normalize newlines to spaces so strings don't have bare \n
    raw = raw.replace(/\n|\r/g, ' ');

    // Fix broken URL protocol: "https: "// -> https://
    raw = raw.replace(/"https?:\s*"\/\//g, '"https://');
    raw = raw.replace(/"http:\s*"\/\//g, '"http://');

    // Collapse double closing braces (}} -> }) — the 1B model often emits these
    raw = raw.replace(/}}/g, '}');

    // Step 2: Extract individual {...} objects via brace-depth tracking (string-aware)
    const objectStrings = [];
    let depth = 0;
    let objStart = -1;
    let inString = false;
    let prevChar = '';

    for (let i = 0; i < raw.length; i++) {
      const ch = raw[i];
      if (ch === '"' && prevChar !== '\\') {
        inString = !inString;
      }
      if (!inString) {
        if (ch === '{') {
          if (depth === 0) objStart = i;
          depth++;
        } else if (ch === '}') {
          depth--;
          if (depth === 0 && objStart !== -1) {
            objectStrings.push(raw.slice(objStart, i + 1));
            objStart = -1;
          }
        }
      }
      prevChar = ch;
    }

    if (objectStrings.length === 0) {
      // Fallback: no objects found — response may be an array of primitive arrays
      // (e.g. similarity pairs: [["Title A", "Title B"], ...])
      const arrStart = raw.indexOf('[');
      const arrEnd = raw.lastIndexOf(']');
      if (arrStart !== -1 && arrEnd !== -1) {
        const candidate = raw.slice(arrStart, arrEnd + 1)
          .replace(/,\s*([\]}])/g, '$1'); // strip trailing commas
        try {
          const parsed = JSON.parse(candidate);
          if (Array.isArray(parsed)) return parsed;
        } catch {}
      }
      console.error('[json-parser] Failed to parse AI response. Raw suspect chunk:');
      console.error(raw.slice(0, 500));
      throw new Error('No JSON objects found in response.');
    }

    // Step 3: Parse each object individually, applying targeted repairs
    const parsed = [];

    for (const objStr of objectStrings) {
      let fixed = objStr;

      // Fix unclosed string values before } or , : "poster": "   } -> "poster": ""
      fixed = fixed.replace(/:\s*"(\s*)(?=[,}])/g, ': ""');

      // Fix values where model wrapped content in movie-style quotes:
      // "desc": ""Quote here." - Person." → "desc": "Quote here. - Person."
      // Pattern: colon + opening-quote + extra-quote + content (stripping any inner ") + closing-quote before ,/}
      fixed = fixed.replace(/:\s*""((?:[^"]|"(?!\s*[,}]))*?)"(?=\s*[,}])/g, (_, inner) => {
        return ': "' + inner.replace(/"/g, '') + '"';
      });

      // Fix possessive: someone"s -> someone's
      fixed = fixed.replace(/(\w)"s\b/g, "$1\u2019s");

      // Protect contractions and possessives (don't, can't, it's, someone's)
      // so the blanket ' -> " step below doesn't break in-string apostrophes.
      fixed = fixed.replace(/(\b\w+)'(\w)/g, "$1\u2019$2");

      // Fix single quotes -> double (must come after possessive/contraction protection)
      fixed = fixed.replace(/'/g, '"');

      // Remove trailing commas before } or ]
      fixed = fixed.replace(/,\s*}/g, '}');
      fixed = fixed.replace(/,\s*]/g, ']');

      // Wrap unquoted scalar values: "director": Christopher Nolan -> "director": "Christopher Nolan"
      fixed = fixed.replace(/:\s*([^"\[\{0-9\-tfn][^,}\]"]*?)(?=[,}\]])/g, (match, p1) => {
        const trimmed = p1.trim();
        if (['true', 'false', 'null'].includes(trimmed)) return `: ${trimmed}`;
        if (trimmed === '') return match;
        return `: "${trimmed.replace(/"/g, '')}"`;
      });

      try {
        parsed.push(JSON.parse(fixed));
      } catch {
        // Also attempt with " escapes flattened (catches bad \" at key boundaries)
        try {
          parsed.push(JSON.parse(fixed.replace(/\\"/g, '"')));
        } catch {
          console.warn('[json-parser] Skipping unparseable object:', fixed.slice(0, 500));
        }
      }
    }

    if (parsed.length === 0) {
      console.warn('[json-parser] No parseable objects found after all repair attempts. Returning empty array.');
      return [];
    }

    return parsed;
  }

  /**
   * Main update loop:
   * 1. Load current movies and similarity pairs.
   * 2. Prompts AI for new movies and similarity links.
   * 3. Sanitizes and merges generated items.
   * 4. Persists the changes to JSON files if not in dry-run mode.
   */
  async run({ count, dryRun, searchQuery }) {
    const currentMovies = this.moviesRepository.readArray();
    const currentSimilarities = this.similaritiesRepository.readArray();
    const existingTitles = currentMovies.map((movie) => movie.title);

    let generatedMovies = [];

    if (searchQuery) {
      console.log(`[movie-updater] Searching the internet for: "${searchQuery}"...`);
      const searchResults = this.searchProvider.search(searchQuery);
      console.log(`[movie-updater] Found ${searchResults.length} related links.`);

      // Prompting AI to turn links into valid JSON movie data
      const searchPrompt = [
        `Convert the following movie search links/titles into detailed movie JSON metadata.`,
        `Query: ${searchQuery}`,
        `Links: ${JSON.stringify(searchResults)}`,
        `Return JSON only. An array of movie objects with: title, director, year, duration, desc, genres, poster.`,
        `- Exclusion list — do not include ANY of these titles: ${JSON.stringify(existingTitles)}`,
        `- Use genres: Action, Drama, Comedy, Sci-Fi, Thriller, Adventure, Horror.`
      ].join('\n');

      const rawSearchResponse = this.ollamaClient.generate(searchPrompt);
      generatedMovies = this.extractJsonArray(rawSearchResponse)
        .map((movie) => MovieValidator.sanitize(movie))
        .filter(Boolean);
    } else {
      // Determine underrepresented genres to target the search
      const VALID_GENRES = ['Action', 'Drama', 'Comedy', 'Sci-Fi', 'Thriller', 'Adventure', 'Horror', 'Romance'];
      const genreCounts = {};
      for (const movie of currentMovies) {
        for (const g of (movie.genres || [])) {
          genreCounts[g] = (genreCounts[g] || 0) + 1;
        }
      }
      const sortedGenres = [...VALID_GENRES].sort((a, b) => (genreCounts[a] || 0) - (genreCounts[b] || 0));

      // Auto-search the internet for movies in underrepresented genres
      const allLinks = [];
      const numQueries = Math.min(count, 3);
      for (let i = 0; i < numQueries; i++) {
        const genre = sortedGenres[i % sortedGenres.length];
        console.log(`[movie-updater] Searching internet for: top ${genre} movies...`);
        const links = this.searchProvider.search(`top ${genre} movies`);
        allLinks.push(...links);
      }
      const uniqueLinks = [...new Set(allLinks)];

      // Pre-filter: strip Wikipedia titles that already exist in the catalog
      // so the LLM only ever sees genuinely new titles to work with.
      const normalizeTitle = (t) => t.toLowerCase().replace(/[^a-z0-9]/g, '');
      const catalogNormalized = new Set(existingTitles.map(normalizeTitle));
      const freshLinks = uniqueLinks.filter((t) => !catalogNormalized.has(normalizeTitle(t)));
      console.log(`[movie-updater] ${freshLinks.length}/${uniqueLinks.length} Wikipedia titles are new to the catalog.`);

      let rawMovieResponse;
      if (freshLinks.length > 0) {
        // Internet-grounded prompt: convert real movie titles to metadata
        const moviePrompt = [
          `Generate JSON metadata for up to ${count} of the following real movies.`,
          `Return JSON only: [ { "title":"...", "director":"...", "year":"...", "duration":"...", "desc":"...", "genres":[...], "poster":"" } ]`,
          'Rules: No markdown. Keep desc under 25 words. No double-quotes inside string values.',
          'Genres: Action, Drama, Comedy, Sci-Fi, Thriller, Adventure, Horror, Romance.',
          `Movie titles to convert: ${JSON.stringify(freshLinks)}`,
        ].join('\n');
        rawMovieResponse = this.ollamaClient.generate(moviePrompt);
      } else {
        // Fallback: pure generation with full exclusion list
        console.log('[movie-updater] Internet search unavailable, falling back to generation.');
        rawMovieResponse = this.ollamaClient.generate(
          this.promptBuilder.buildMoviePrompt(existingTitles, count)
        );
      }

      generatedMovies = this.extractJsonArray(rawMovieResponse)
        .map((movie) => MovieValidator.sanitize(movie))
        .filter(Boolean)
        .slice(0, count);
    }

    // Fetch missing posters for newly generated movies
    if (this.posterFetcher && generatedMovies.length > 0) {
      for (const movie of generatedMovies) {
        if (!movie.poster || movie.poster.trim() === '') {
          movie.poster = await this.posterFetcher.fetch(movie.title, movie.year);
        }
      }
    }

    const mergedMovies = this.catalogMerger.dedupeMoviesByTitle([...currentMovies, ...generatedMovies]);
    const newMovies = mergedMovies.slice(currentMovies.length);

    // Second phase: compute similarity pairs via genre overlap (deterministic, no LLM)
    const generatedSimilarities = SimilarityComputer.computePairs(newMovies, mergedMovies);

    const mergedSimilarities = this.catalogMerger.mergeSimilarities(
      currentSimilarities,
      generatedSimilarities,
      mergedMovies.map((movie) => movie.title)
    );

    // Write-back phase
    if (!dryRun) {
      this.moviesRepository.writeArray(mergedMovies);
      this.similaritiesRepository.writeArray(mergedSimilarities);

      if (newMovies.length > 0) {
        console.log(`[movie-updater] Added ${newMovies.length} new movies:`);
        newMovies.forEach((m) => console.log(`  - ${m.title} (${m.year}) by ${m.director}`));
      }

      const newSims = mergedSimilarities.slice(currentSimilarities.length);
      if (newSims.length > 0) {
        console.log(`[movie-updater] Added ${newSims.length} new similarity links:`);
        newSims.forEach(([a, b]) => console.log(`  - ${a} <-> ${b}`));
      }
    }

    return {
      generatedMovieCount: generatedMovies.length,
      addedMovieCount: mergedMovies.length - currentMovies.length,
      totalMovieCount: mergedMovies.length,
      generatedSimilarityCount: generatedSimilarities.length,
      addedSimilarityCount: mergedSimilarities.length - currentSimilarities.length,
      totalSimilarityCount: mergedSimilarities.length,
      dryRun
    };
  }
}

/**
 * Parses command-line arguments to resolve config.
 */
function parseArgs(argv) {
  const options = {
    count: 5,
    model: process.env.OLLAMA_MODEL || 'cinematch-movie-curator',
    moviesFile: path.resolve(__dirname, '../../src/db/movies.json'),
    similaritiesFile: path.resolve(__dirname, '../../src/db/similarities.json'),
    dryRun: false,
    search: null
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '--count') options.count = Number.parseInt(argv[index + 1], 10);
    if (arg === '--model') options.model = argv[index + 1];
    if (arg === '--movies-file') options.moviesFile = path.resolve(argv[index + 1]);
    if (arg === '--similarities-file') options.similaritiesFile = path.resolve(argv[index + 1]);
    if (arg === '--dry-run') options.dryRun = true;
    if (arg === '--search') options.search = argv[index + 1];
  }

  if (!Number.isInteger(options.count) || options.count < 1 || options.count > 25) {
    throw new Error('`--count` must be an integer between 1 and 25.');
  }

  return options;
}

/**
 * Script entry point.
 */
async function main() {
  const options = parseArgs(process.argv.slice(2));

  // Detect the available Python executable (handles WSL, Linux, and Windows environments)
  const PYTHON_PATH = (() => {
    const candidates = ['python3', 'python', 'C:/Users/sully/AppData/Local/Microsoft/WindowsApps/python3.13.exe'];
    for (const cmd of candidates) {
      try {
        execFileSync(cmd, ['--version'], { stdio: 'ignore' });
        return cmd;
      } catch { /* not available */ }
    }
    return candidates[0]; // best guess fallback
  })();

  const updater = new MovieCatalogUpdater({
    moviesRepository: new JsonFileRepository(options.moviesFile, 'Movies'),
    similaritiesRepository: new JsonFileRepository(options.similaritiesFile, 'Similarities'),
    ollamaClient: new OllamaClient(options.model),
    promptBuilder: new PromptBuilder(),
    catalogMerger: new CatalogMerger(),
    searchProvider: new SearchProvider(PYTHON_PATH),
    posterFetcher: new PosterFetcher()
  });

  const result = await updater.run({
    count: options.count,
    dryRun: options.dryRun,
    searchQuery: options.search
  });

  console.log(
    `[movie-updater] movies_generated=${result.generatedMovieCount} movies_added=${result.addedMovieCount} movies_total=${result.totalMovieCount} similarities_generated=${result.generatedSimilarityCount} similarities_added=${result.addedSimilarityCount} similarities_total=${result.totalSimilarityCount} dryRun=${result.dryRun}`
  );
}

